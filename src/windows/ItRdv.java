     private void loadMedecin() {
        List<Medecin> medecinList = im.allMedecin();

        for (Medecin medecin : medecinList) {
            medecin_cbx.addItem(medecin.getNom());
            hm.put(medecin.getNom(), medecin);
        }
        medecin_cbx.setSelectedIndex(-1);
        }
        
        private void loadPatient() {
        List<Patient> patientList = ip.allPatient();

        for (Patient patient : patientList) {
            patient_cbx.addItem(patient.getNom());
            hms.put(patient.getNom(), patient);
        }
        patient_cbx.setSelectedIndex(-1);
       }
         private void affichage() {
        
        rdvList = ie.allItRdv();
        
        List<String> elementOmit = new ArrayList<>();
        elementOmit.add("id"); //pour exclure l'id de l'affiehage du tableau       
        UtilsFonction.displayDataInTable(rdvList, affichage, elementOmit);
    }
        

        private void clearFied() {
        motif_txt.setText("");     
       date_txt.setDate(null);
        patient_cbx.setSelectedIndex(-1);
        medecin_cbx.setSelectedIndex(-1);
    }


