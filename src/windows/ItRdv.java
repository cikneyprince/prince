    private void ajouter_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouter_btnActionPerformed
        // TODO add your handling code here:
            String motifConsultation = motif_txt.getText();
            Date dateHeure = date_txt.getDate();
            Medecin medecinId = hm.get(medecin_cbx.getSelectedItem().toString());
            Patient patientId = hms.get(patient_cbx.getSelectedItem().toString());
       

           ItRdv rd = new ItRdv(motifConsultation, dateHeure, medecinId, patientId);

           Patient rv = ir.addPatient(rd);

            if (rv.getId() == null) {
                JOptionPane.showMessageDialog(this, "Erreur lors de l'insertion");
                return;
            }else{
             JOptionPane.showMessageDialog(this, "Insertion reuissi avec success");
             clearFied();
            }
        
        affichage();
              
    }//GEN-LAST:event_ajouter_btnActionPerformed

